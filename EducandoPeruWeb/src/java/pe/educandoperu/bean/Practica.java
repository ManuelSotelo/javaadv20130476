package pe.educandoperu.bean;


public class Practica {
    private Integer codPractica;
    private String nomPractica;
    private String codTema;

    public Integer getCodPractica() {
        return codPractica;
    }

    public void setCodPractica(Integer codPractica) {
        this.codPractica = codPractica;
    }

    public String getNomPractica() {
        return nomPractica;
    }

    public void setNomPractica(String nomPractica) {
        this.nomPractica = nomPractica;
    }

    public String getCodTema() {
        return codTema;
    }

    public void setCodTema(String codTema) {
        this.codTema = codTema;
    }
}
