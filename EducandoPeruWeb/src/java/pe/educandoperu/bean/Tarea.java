package pe.educandoperu.bean;


public class Tarea {
    private Integer codTarea;
    private String nomTarea;
    private String codTema;

    public Integer getCodTarea() {
        return codTarea;
    }

    public void setCodTarea(Integer codTarea) {
        this.codTarea = codTarea;
    }

    public String getNomTarea() {
        return nomTarea;
    }

    public void setNomTarea(String nomTarea) {
        this.nomTarea = nomTarea;
    }

    public String getCodTema() {
        return codTema;
    }

    public void setCodTema(String codTema) {
        this.codTema = codTema;
    }
    
}
